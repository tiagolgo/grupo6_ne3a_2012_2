    private void desmarcaTabela() {
        int l = this.tabela_Grade.getSelectedRow();
        this.tabela_Grade.removeRowSelectionInterval(l, l);
        this.tabela_Grade.setFocusable(false);
    }

    private void limpaCampos() {
        this.select_Disciplina.setSelectedIndex(0);
        this.select_Série.setSelectedIndex(0);
        this.select_Curso.setSelectedIndex(0);
        this.select_CH.setSelectedIndex(0);
    }

    private boolean verificaCampos() {
        Object d = this.select_Disciplina.getSelectedItem();
        Object s = this.select_Série.getSelectedItem();
        Object c = this.select_Curso.getSelectedItem();
        Object ch = this.select_CH.getSelectedItem();
        if (d.equals("") || s.equals("") || c.equals("") || ch.equals("")) {
            JOptionPane.showMessageDialog(null, "Há campo(s) de seleção vazio(s)!");
            this.insere_Grade.transferFocus();
            return false;
        }
        return true;

    }

