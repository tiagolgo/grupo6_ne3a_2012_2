    private void insere_GradeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insere_GradeActionPerformed
        // TODO add your handling code here:
                javax.swing.table.DefaultTableModel model;
                if (verificaCampos()) {
                    model = (DefaultTableModel) this.tabela_Grade.getModel();
                    model.addRow(new Object[]{
                                this.select_Disciplina.getSelectedItem(),
                                this.select_Série.getSelectedItem(),
                                this.select_Curso.getSelectedItem(),
                                this.select_CH.getSelectedItem()});
                    limpaCampos();
        //            this.select_Curso.setFocusable(true);
                    this.select_Curso.requestFocusInWindow();
                }
    }//GEN-LAST:event_insere_GradeActionPerformed

